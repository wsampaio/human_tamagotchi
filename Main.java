	public void iteraction(){

		if (getIMC() < 8)
			death();

		if (!live)
			return;

		plusIdade();

		plusFome();

		plusAltura();

		if (fome > 0.5)
			minusPeso();

		minusSaude();


	};

	double getIMC(){
		return peso / (altura*altura);
	}

	void plusIdade(){
		idade += 0.1 * proporcao;
	}

	void plusFome(){
		fome += 0.1 * proporcao;

		if (fome > 1)
			fome = 1;
	}

	void plusAltura(){
		// crescimento
		if (idade < 2)
			altura += 0.021 * proporcao * Math.pow(saude, 0.25);
		else if (idade < 9)
			altura += 0.004 * proporcao * Math.pow(saude, 0.25);
		else if (idade < 14)
			altura += 0.015 * proporcao * Math.pow(saude, 0.25);
		else if (idade < 18)
			altura += 0.004 * proporcao * Math.pow(saude, 0.25);
	}

	void minusPeso(){
		peso -= 1 * proporcao;
	}

	void plusSaude(){
		saude += 0.1;

		if (saude > 1)
			saude = 1;
	}

	void minusSaude(){

		if (getIMC() < 18.5) // Magreza
			saude -= 0.2 * proporcao;
		else if (getIMC() < 25.0) // Peso normal
			saude += 0.0 * proporcao;
		else if (getIMC() < 30.0) // Sobrepeso
			saude -= 0.02 * proporcao;
		else if (getIMC() < 35.0) // Obesidade grau I
			saude -= 0.05 * proporcao;
		else if (getIMC() < 40.0) // Obesidade grau II
			saude -= 0.1 * proporcao;
		else // Obesidade grau III
			saude -= 0.15 * proporcao;

		if (fome > 0.8)
			saude -= 0.2 * proporcao;

		if (saude < 0.1)
			saude = 0.1;
		
		//saude = 1;
	}

	void death(){
		live = false;
	}

	@Override
	public boolean isLive(){
		return live;
	};

