class GameAction{

	PrintTela printTela;
	List<Imprimivel> objetoLista;
	Scanner scanner = new Scanner(System.in);
	Hero player;
	String console = "";
	int timerLen = 1000;
	Timer t;
	TimerTask tt ;

	public GameAction(List<Imprimivel> objetoLista){

		this.objetoLista = objetoLista;
		printTela = new PrintTela(objetoLista);
		scanner = new Scanner(System.in);

		for(Imprimivel o : objetoLista)
			if (o instanceof Hero)
				player = (Hero) o;
		
		setTimer();

		while(scanner.hasNext()){
			console = scanner.next();

			System.out.println("Token: " + console);
			if (console.equals("q")){
				//System.out.print("\033\143");
				System.exit(0);
			}
			
			if (console.equals("c")){
				player.comer();
			}
			
			if (console.equals("e")){
				player.exercicios();
			}
		}
		//scanner.close();
	}

	void setTimer(){

		t = new Timer();
		tt = new TimerTask(){		
			@Override
			public void run(){
				runAction();
			}
		};

		t.schedule(tt, new Date(), timerLen);
	}

	void runAction(){
		
		if (!player.isLive()){
			console = "He's Dead!";
		} else {
			player.iteraction();
		}

		printTela.print(console);
	};

}

