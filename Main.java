class PrintTela{

	List<Imprimivel> objetoLista;

	public PrintTela(List<Imprimivel> objetoLista){
		this.objetoLista = objetoLista;
	}

	public void print(String console){

		System.out.print("\033\143");
		char charAtual = ' ';

		for(int i=0;i<objetoLista.get(0).getDesenho().length;i++){
			for(int j=0;j<objetoLista.get(0).getDesenho()[i].length;j++){
				charAtual = ' ';

				for (Imprimivel o : objetoLista)
					if (o.getDesenho()[i][j] != ' ')
						charAtual = o.getDesenho()[i][j];

				System.out.print(charAtual);
			}
			System.out.print("\n");
		}

		System.out.println(
			"console: " + console +
			"\n player: " + objetoLista.get(1) +
			""
		);
	}
}

